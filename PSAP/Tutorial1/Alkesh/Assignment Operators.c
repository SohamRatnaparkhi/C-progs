#include <stdio.h>
// In this C assignment operators Program.
// We are using two integer variables a, Total, and their values are 7 and 21. 
//Next, we are going to use these two variables to show  the working  
//of all the Assignment Operators in C Programming Language

int main()
{
 int a = 7;
 int Total = 21;

 printf(" Value of the Total = %d \n", Total += a );
 printf(" Value of the Total = %d \n", Total -= a );
 printf(" Value of the Total = %d \n", Total *= a );
 printf(" Value of the Total = %d \n", Total /= a );
 printf(" Value of the Total = %d \n", Total %= a );

 return 0;
}
